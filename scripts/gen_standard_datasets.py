# ═══════════════════════════════════════════════════════════════
# Helper: write JSON
# ═══════════════════════════════════════════════════════════════

def write_ds(name: str, scale: str, data: dict):
    p = DATA_DIR / f"{name}_{scale}.json"
    p.write_text(json.dumps(data, indent=2, ensure_ascii=False), encoding="utf-8")
    ss = data["sessions"]
    qs = data["questions"]
    core = sum(1 for s in ss if s.get("metadata", {}).get("type") == "core")
    noise = sum(1 for s in ss if s.get("metadata", {}).get("type") == "noise")
    cats = {}
    for q in qs:
        c = q["category"]
        cats[c] = cats.get(c, 0) + 1
    print(f"  {p.name:45s} sess={len(ss):4d}(C{core}/N{noise})  qs={len(qs):4d}  cats={cats}")


