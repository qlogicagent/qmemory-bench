# ═══════════════════════════════════════════════════════════════
# Timestamp generator (spread over a year)
# ═══════════════════════════════════════════════════════════════

def gen_timestamps(n: int, start="2025-01-10", end="2026-01-10") -> list[str]:
    s = datetime.fromisoformat(start)
    e = datetime.fromisoformat(end)
    delta = (e - s) / max(n, 1)
    return [(s + delta * i).strftime("%Y-%m-%dT%H:%M:%SZ") for i in range(n)]


